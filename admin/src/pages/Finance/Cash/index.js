@connect(({ extract, loading }) => ({
  data: extract.data,
  loading: loading.models.extract,
}))
@Form.create()
export default class extends PureComponent {
  state = {
    modalVisible: false,
    updateModalVisible: false,
    expandForm: false,
    selectedRows: [],
    formValues: {},
    stepFormValues: {},
    visible: false,
    selectedRow: null,
  };

  componentDidMount() {
    this.refresh();
  }

  refresh = page => {
    const { dispatch } = this.props;
    dispatch({
      type: 'extract/fetch',
      payload: {
        page,
      },
    });
  };

  reload = () => {
    this.refresh(0);
  };

  renderActionMenu = row => {
    return (
      <Menu onClick={e => this.handleActionMenuClick(e, row)}>
        <Menu.Item key={3}>驳回</Menu.Item>
        <Menu.Item key={2}>通过</Menu.Item>
      </Menu>
    );
  };

  handleActionMenuClick = (e, row) => {
    const { dispatch } = this.props;

    if (e.key == 3) {
      this.setState(state => ({ ...state, visible: true, selectedRow: row }));
      return false;
    }

    dispatch({
      type: 'extract/update',
      payload: {
        row,
        values: {
          status: e.key,
        },
      },
    });
  };

  handleStandardTableChange = (pagination, filtersArg, sorter) => {
    const { dispatch } = this.props;
    const { formValues } = this.state;

    const filters = Object.keys(filtersArg).reduce((obj, key) => {
      const newObj = { ...obj };
      newObj[key] = getValue(filtersArg[key]);
      return newObj;
    }, {});

    const params = {
      currentPage: pagination.current,
      pageSize: pagination.pageSize,
      ...formValues,
      ...filters,
    };
    if (sorter.field) {
      params.sorter = `${sorter.field}_${sorter.order}`;
    }

    dispatch({
      type: 'rule/fetch',
      payload: params,
    });
  };

  previewItem = id => {
    router.push(`/profile/basic/${id}`);
  };

  handleFormReset = () => {
    const { form, dispatch } = this.props;
    form.resetFields();
    this.setState({
      formValues: {},
    });
    dispatch({
      type: 'rule/fetch',
      payload: {},
    });
  };

  toggleForm = () => {
    const { expandForm } = this.state;
    this.setState({
      expandForm: !expandForm,
    });
  };

  handleMenuClick = e => {
    const { dispatch } = this.props;
    const { selectedRows } = this.state;

    if (selectedRows.length === 0) return;
    switch (e.key) {
      case 'remove':
        dispatch({
          type: 'rule/remove',
          payload: {
            key: selectedRows.map(row => row.key),
          },
          callback: () => {
            this.setState({
              selectedRows: [],
            });
          },
        });
        break;
      default:
        break;
    }
  };

  handleSelectRows = rows => {
    this.setState({
      selectedRows: rows,
    });
  };

  handleSearch = e => {
    e.preventDefault();

    const { dispatch, form } = this.props;

    form.validateFields((err, fieldsValue) => {
      if (err) return;

      const values = {
        ...fieldsValue,
        updatedAt: fieldsValue.updatedAt && fieldsValue.updatedAt.valueOf(),
      };

      this.setState({
        formValues: values,
      });

      dispatch({
        type: 'rule/fetch',
        payload: values,
      });
    });
  };

  handleModalVisible = flag => {
    this.setState({
      modalVisible: !!flag,
    });
  };

  handleUpdateModalVisible = (flag, record) => {
    this.setState({
      updateModalVisible: !!flag,
      stepFormValues: record || {},
    });
  };

  handleUpdate = fields => {
    const { dispatch } = this.props;
    const { formValues } = this.state;
    dispatch({
      type: 'rule/update',
      payload: {
        query: formValues,
        body: {
          name: fields.name,
          desc: fields.desc,
          key: fields.key,
        },
      },
    });

    message.success('配置成功');
    this.handleUpdateModalVisible();
  };

  renderForm() {
    const {
      form: { getFieldDecorator },
    } = this.props;
    return (
      <Form onSubmit={this.handleSearch} layout="inline">
        <Row gutter={{ md: 8, lg: 24, xl: 48 }}>
          <Col md={8} sm={24}>
            <FormItem label="关键词">
              {getFieldDecorator('name')(<Input placeholder="请输入关键词" />)}
            </FormItem>
          </Col>
        </Row>
        <Row gutter={{ md: 8, lg: 24, xl: 48 }}>
          <Col md={8} sm={24}>
            <FormItem label="实名状态">
              {getFieldDecorator('status')(
                <Select placeholder="请选择" style={{ width: '100%' }}>
                  <Option value="0">关闭</Option>
                  <Option value="1">运行中</Option>
                </Select>
              )}
            </FormItem>
          </Col>
          <Col md={8} sm={24}>
            <FormItem label="账户状态">
              {getFieldDecorator('status3')(
                <Select placeholder="请选择" style={{ width: '100%' }}>
                  <Option value="0">关闭</Option>
                  <Option value="1">运行中</Option>
                </Select>
              )}
            </FormItem>
          </Col>
        </Row>
        <div style={{ overflow: 'hidden' }}>
          <div style={{ marginBottom: 24 }}>
            <Button type="primary" htmlType="submit">
              查询
            </Button>
            <Button style={{ marginLeft: 8 }} onClick={this.handleFormReset}>
              重置
            </Button>
          </div>
        </div>
      </Form>
    );
  }

  render() {
    const { data, loading } = this.props;
    const { selectedRows, modalVisible, updateModalVisible, stepFormValues } = this.state;

    const menu = (
      <Menu onClick={this.handleMenuClick} selectedKeys={[]}>
        <Menu.Item key="remove">删除</Menu.Item>
        <Menu.Item key="approval">批量审批</Menu.Item>
      </Menu>
    );

    const parentMethods = {
      handleAdd: this.handleAdd,
      handleModalVisible: this.handleModalVisible,
    };
    const updateMethods = {
      handleUpdateModalVisible: this.handleUpdateModalVisible,
      handleUpdate: this.handleUpdate,
    };
    return (
      <PageHeaderWrapper title="提现记录">
        <Modal
          title="驳回理由"
          visible={this.state.visible}
          onOk={() => {
            const rejectMsg = this.rejectMsgRef.input.value;
            if (!rejectMsg) {
              message.error('请填写驳回理由！');
              return false;
            }
            this.props.dispatch({
              type: 'extract/update',
              payload: {
                row: this.state.selectedRow,
                values: {
                  status: 3,
                  remarks: rejectMsg,
                },
              },
            });
            this.setState(state => ({ ...state, visible: false }));
          }}
          onCancel={() => this.setState(state => ({ ...state, visible: false }))}
        >
          <Input placeholder="请输入驳回理由" ref={e => (this.rejectMsgRef = e)} />
        </Modal>
        <Card bordered={false}>
          <div className={styles.tableList}>
            <div className={styles.tableListForm}>{this.renderForm()}</div>
            <div className={styles.tableListOperator}>
              {selectedRows.length > 0 && (
                <span>
                  <Dropdown overlay={menu}>
                    <Button>
                      更多操作 <Icon type="down" />
                    </Button>
                  </Dropdown>
                </span>
              )}
            </div>
            <StandardTable
              selectedRows={selectedRows}
              loading={loading}
              data={data}
              columns={[
                {
                  title: '订单号',
                  dataIndex: 'id',
                  render: text => <a onClick={() => this.previewItem(text)}>{text}</a>,
                },
                {
                  title: '开户人',
                  dataIndex: 'userName',
                },
                {
                  title: '银行卡号',
                  dataIndex: 'bankCardCode',
                },
                {
                  title: '银行类型',
                  dataIndex: 'bankCardType',
                },
                {
                  title: '开户支行',
                  dataIndex: 'bankName',
                },
                {
                  title: '金额',
                  dataIndex: 'money',
                },
                {
                  title: '状态',
                  dataIndex: 'status',
                  render(val) {
                    return <Badge status={statusMap[val]} text={status[val]} />;
                  },
                },
                {
                  title: '下单时间',
                  dataIndex: 'createAt',
                  render: val => <span>{moment(val).format('YYYY-MM-DD HH:mm:ss')}</span>,
                },
                {
                  title: '操作',
                  render: (text, record) => (
                    <Fragment>
                      <Dropdown overlay={() => this.renderActionMenu(record)}>
                        <Button style={{ marginLeft: 8 }}>
                          审核 <Icon type="down" />
                        </Button>
                      </Dropdown>
                    </Fragment>
                  ),
                },
              ]}
              expandedRowRender={record => (
                <p>
                  支付凭证:{' '}
                  <img style={{ maxWidth: 400 }} src={config.UPLOAD_PATH + record.certificate} />
                </p>
              )}
              onSelectRow={this.handleSelectRows}
              onChange={this.handleStandardTableChange}
            />

            <Row style={{ marginTop: 20 }} type="flex" justify="end">
              <Col>
                <Pagination
                  defaultCurrent={1}
                  current={data.page}
                  pageSize={data.pageSize}
                  total={data.total}
                  hideOnSinglePage={true}
                  onChange={page => this.refresh(page)}
                />
              </Col>
            </Row>
          </div>
        </Card>
        <CreateForm {...parentMethods} modalVisible={modalVisible} />
        {stepFormValues && Object.keys(stepFormValues).length ? (
          <UpdateForm
            {...updateMethods}
            updateModalVisible={updateModalVisible}
            values={stepFormValues}
          />
        ) : null}
      </PageHeaderWrapper>
    );
  }
}
